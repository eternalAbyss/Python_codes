# Variable declaration 
# Using a normal convention we name the python variables with small case alphabets and '_' for the space
a, b = 10, 3

# Addition
print(a+b)

# Subtraction
print(a-b)

# Multiplication
print(a*b)

# Division 
print(a/b)

# Rounded Division
print(a//b)

# Exponentiation
print(a**b)

# modulo 
print(a%b)

# Bitwise operators

# Bitwise XOR 
print(1 ^ 0)

# Comparison operators
print(a>b)

print(a<b)

print(a>=b)

print(a<=b)

# Logical Operators
print(True and False)

print(True or False)

print(not True)


